public class Constantes {
    public static void main(String[] args) {
        final double PI = 3.14159; // Declaración de una constante para el valor de Pi
        double radio = 5; // Radio del círculo
        double area = PI * radio * radio; // Cálculo del área usando la constante
            // Imprimir el resultado
        System.out.println("El área del círculo es: " + area);
    }
}
